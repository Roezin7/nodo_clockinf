export type KioskMessageKey = keyof (typeof kioskMessages)['es'];

export function normalizeKioskLanguage(value: string | null | undefined): KioskLanguage {
  return value === 'en' ? 'en' : 'es';
}
export function kioskText(language: KioskLanguage, key: KioskMessageKey): string {
  return kioskMessages[language][key];
}
